#5 funkcji predefiniowanych

#1. input() - Pozwala na pobranie danych od użytkownika
#2. len() - Zwraca długość sekwencji, takiej jak łancuch znaków czy lista.
#3. print() - Służy do wyświetlania danych w ekranie konsoli
#4. range() - Generuje sekwencje liczb całkowitych
#5. abs() - Zwraca wartość bezwzględną liczby